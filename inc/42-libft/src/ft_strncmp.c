#include "libft.h"

int	ft_strncmp(char *s1, char *s2, size_t size)
{
	size_t	i;

	if (!size)
		return (0);
	i = 0;
	while (i < size - 1 && s1[i] && s1[i] == s2[i])
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
